//字符串链接符的演示
public class StringConnectorDemo {
    public static void main(String[] args) {
        String name = "zjw";
        int age = 24;
        System.out.println("大家好，我叫" + name + "，今年" + age + "岁了.");
        System.out.println(name + "我今年" + age + "岁");
    }
}
